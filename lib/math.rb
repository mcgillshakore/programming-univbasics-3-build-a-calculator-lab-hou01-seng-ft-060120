def addition(num1, num2)
sum = num2 + num1
sum
end

def subtraction(num1, num2)
sub = num1 - num2
sub
end

def division(num1, num2)
divide = num1 / num2
divide
end

def multiplication(num1, num2)
  multiple = num1 * num2
  multiple
end

def modulo(num1, num2)
  percent = num1 % num2
  percent
end

def square_root(num)
  sum = Math.sqrt(num)
  sum
end